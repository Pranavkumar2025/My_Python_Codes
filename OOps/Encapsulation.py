class Computer:

# private attributes using underscore as the prefix i.e single _ or double __
    
    # by using underscore _ as the prefix we can able to use Encapsulation in python.
    def __init__(self):
        self.__maxprice = 900

    def sell(self):
        print("Selling Price: {}".format(self.__maxprice))

    def setMaxPrice(self, price):
        self.__maxprice = price

c = Computer()
c.sell()

# change the price
c.__maxprice = 1000
c.sell()

# using setter function
c.setMaxPrice(1000)
c.sell()